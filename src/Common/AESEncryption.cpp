void AESEncryption::rotate(uint8_t *word) {
	int32_t i;
	uint8_t c = word[0];
	for (i = 0; i < 3; i++) {
		word[i] = word[i + 1];
	}
	word[3] = c;
}

void AESEncryption::core(uint8_t *word, int32_t iteration) {
	int32_t i;
	rotate(word);
	for (i = 0; i < 4; i++) {
		word[i] = m_sbox[word[i]];
	}
	word[0] = word[0] ^ m_rcon[iteration];
}

void AESEncryption::expandKey(uint8_t *expandedKey, size_t expandedKeySize) {
	int32_t currentSize = 0;
	int32_t rconIteration = 1;
	int32_t i;
	uint8_t t[4] = {0}; 
	for (i = 0; i < AESKEYSIZE; i++) {
		expandedKey[i] = m_aeskey[i];
	}
	currentSize += AESKEYSIZE;
	while ((uint32_t)currentSize < (uint32_t)expandedKeySize) {
		for (i = 0; i < 4; i++) {
			t[i] = expandedKey[(currentSize - 4) + i];
		}
		if (currentSize % AESKEYSIZE == 0) {
			core(t, rconIteration++);
		}
		if (AESKEYSIZE == 32 && ((currentSize % AESKEYSIZE) == 16)) {
			for (i = 0; i < 4; i++) {
				t[i] = m_sbox[t[i]];
			}
		}
		for (i = 0; i < 4; i++) {
			expandedKey[currentSize] = expandedKey[currentSize - AESKEYSIZE] ^ t[i];
			currentSize++;
		}
	}
}

void AESEncryption::subBytes(uint8_t *state) {
	int32_t i;
	for (i = 0; i < 16; i++) {
		state[i] = m_sbox[state[i]];
	}
}

void AESEncryption::shiftRow(uint8_t *state, uint8_t nbr) {
	int32_t i, j;
	uint8_t tmp;
	for (i = 0; i < nbr; i++) {
		tmp = state[0];
		for (j = 0; j < 3; j++) {
			state[j] = state[j + 1];
		}
		state[3] = tmp;
	}
}

void AESEncryption::shiftRows(uint8_t *state) {
	int8_t i;
	for (i = 0; i < 4; i++) {
		shiftRow(state + i * 4, i);
	}
}


void AESEncryption::addRoundKey(uint8_t *state, uint8_t *roundKey) {
	int8_t i;
	for (i = 0; i < 16; i++) {
		state[i] = state[i] ^ roundKey[i];
	}
}

uint8_t AESEncryption::galois_multiplication(uint8_t a, uint8_t b) {
	uint8_t p = 0;
	uint8_t counter;
	uint8_t hi_bit_set;
	for (counter = 0; counter < 8; counter++) {
		if ((b & 1) == 1)
			p ^= a;
		hi_bit_set = (a & 0x80);
		a <<= 1;
		if (hi_bit_set == 0x80)
			a ^= 0x1b;
		b >>= 1;
	}
	return p;
}

void AESEncryption::mixColumn(uint8_t *column) {
	uint8_t cpy[4];
	int32_t i;
	for (i = 0; i < 4; i++) {
		cpy[i] = column[i];
	}
	column[0] = galois_multiplication(cpy[0], 2) ^
				galois_multiplication(cpy[3], 1) ^
				galois_multiplication(cpy[2], 1) ^
				galois_multiplication(cpy[1], 3);

	column[1] = galois_multiplication(cpy[1], 2) ^
				galois_multiplication(cpy[0], 1) ^
				galois_multiplication(cpy[3], 1) ^
				galois_multiplication(cpy[2], 3);

	column[2] = galois_multiplication(cpy[2], 2) ^
				galois_multiplication(cpy[1], 1) ^
				galois_multiplication(cpy[0], 1) ^
				galois_multiplication(cpy[3], 3);

	column[3] = galois_multiplication(cpy[3], 2) ^
				galois_multiplication(cpy[2], 1) ^
				galois_multiplication(cpy[1], 1) ^
				galois_multiplication(cpy[0], 3);
}

void AESEncryption::mixColumns(uint8_t *state) {
	int32_t i, j;
	uint8_t column[4];
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 4; j++) {
			column[j] = state[j * 4 + i];
		}
		mixColumn(column);
		for (j = 0; j < 4; j++) {
			state[j * 4 + i] = column[j];
		}
	}
}

void AESEncryption::aes_round(uint8_t *state, uint8_t *roundKey) {
	subBytes(state);
	shiftRows(state);
	mixColumns(state);
	addRoundKey(state, roundKey);
}

void AESEncryption::createRoundKey(uint8_t *expandedKey, uint8_t *roundKey) {
	int32_t i,j;
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 4; j++) {
			roundKey[i + j * 4] = expandedKey[i * 4 + j];
		}
	}
}

void AESEncryption::aes_main(uint8_t *state, uint8_t *expandedKey, int32_t nbrRounds) {
	int32_t i = 0;
	uint8_t roundKey[16];
	createRoundKey(expandedKey, roundKey);
	addRoundKey(state, roundKey);
	for (i = 1; i < nbrRounds; i++) {
		createRoundKey(expandedKey + 16 * i, roundKey);
		aes_round(state, roundKey);
	}
	createRoundKey(expandedKey + 16 * nbrRounds, roundKey);
	subBytes(state);
	shiftRows(state);
	addRoundKey(state, roundKey);
}

int8_t AESEncryption::aes_encrypt(uint8_t *input, uint8_t *output) {
	uint8_t *expandedKey;
	uint8_t block[16];
	int32_t i, j, nbrRounds, expandedKeySize;
	switch (AESKEYSIZE) {
		case 16:
			nbrRounds = 10;
			break;
		case 24:
			nbrRounds = 12;
			break;
		case 32:
			nbrRounds = 14;
			break;
		default:
			return -2;
			break;
	}
	expandedKeySize = 16 * (nbrRounds + 1);
	expandedKey = new uint8_t[expandedKeySize * sizeof(int8_t)];
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 4; j++) {
			block[i + j * 4] = input[i * 4 + j];
		}
	}
	expandKey(expandedKey, expandedKeySize);
	aes_main(block, expandedKey, nbrRounds);
	for (i = 0; i < 4; i++) {
		for (j = 0; j < 4; j++) {
			output[i * 4 + j] = block[i + j * 4];
		}
	}
	delete[] expandedKey;
	return 0;
}

void AESEncryption::decryptofb(uint8_t *buffer, uint8_t *vec, int32_t bsize) {
	uint8_t input[16] = {0};
	uint8_t output[16] = {0};
	uint8_t plaintext[16] = {0};
	uint8_t IV[16];
	int32_t i, j, iter;
	int32_t passes = bsize / 0x10;
	int32_t remainder = bsize % 0x10;
	for (i = 0; i < 16; i++) {
		IV[i] = vec[i % 4];
	}
	for (j = 0; j < (passes + 1); j++) {
		iter = j * 0x10;
		aes_encrypt((j == 0 ? IV : input), output);
		for (i = 0; (i < 0x10) && ((iter + i) < bsize); i++) {
			plaintext[i] = output[i] ^ buffer[iter + i];
		}
		memcpy(buffer + iter, plaintext, (j == passes ? remainder : 0x10));
		memcpy(input, output, 0x10 * sizeof(uint8_t));
	}
}
